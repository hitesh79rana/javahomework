package javahomework;

public class WhileEvenOddNumbers
{
    public static void main(String[] args)
    {
     int h = 2; int r = 1;
     while (h<=22) {
         System.out.println(h + " EVEN NUMBER"); h = h + 2;
     }
     while (r<=19)
     {
         System.out.println(r + " ODD NUMBER"); r = r + 2;
     }
    }
}
